// calculate the average of all elements in an array

let arr = [1, 2, 3, 4, 5];

function averageAll(arr){
    let total = 0;
    for(let i = 0; i < arr.length; i++){
        total = total + arr[i];
    }
    return total / arr.length;
}
console.log(averageAll(arr));

